int	ft_atoi(char *str)
{
	int				res;
	int				pos;
	unsigned int	i;

	pos = 1;
	i = 0;
	res = 0;
	if (str[i] == '-')
	{
		i++;
		pos = -1;
	}
	while (str[i])
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * pos);
}
